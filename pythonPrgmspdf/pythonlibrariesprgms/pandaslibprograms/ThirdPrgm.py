import pandas as pd

ps1 = pd.Series([2, 4, 6, 8, 10])
ps2 = pd.Series([1, 3, 5, 7, 9])
print("Addition two Pandas Series: ")
print(ps1 + ps2)

print("Subtraction two Pandas Series: ")
print(ps1 - ps2)

print("Multiplication two Pandas Series: ")
print(ps1 * ps2)

print("Division two Pandas Series: ")
print(ps1 / ps2)


